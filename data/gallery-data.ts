/**
 * Get the total number of gallery items
 */
export function getGalleryItemCount(): number {
  return galleryData.length;
}

/**
 * Find a gallery item by its slug
 */
export function getGalleryItemBySlug(slug: string): GalleryItem | undefined {
  return galleryData.find((item) => item.slug === slug);
}

/**
 * Get the index of an item by its slug
 */
export function getGalleryItemIndex(slug: string): number {
  return galleryData.findIndex((item) => item.slug === slug);
}

/**
 * Get the previous gallery item given a slug
 */
export function getPreviousGalleryItem(slug: string): GalleryItem | undefined {
  const index = getGalleryItemIndex(slug);
  if (index <= 0) return undefined;
  return galleryData[index - 1];
}

/**
 * Get the next gallery item given a slug
 */
export function getNextGalleryItem(slug: string): GalleryItem | undefined {
  const index = getGalleryItemIndex(slug);
  if (index === -1 || index >= galleryData.length - 1) return undefined;
  return galleryData[index + 1];
}

/**
 * Generate the thumbnail image URL for a gallery item
 */
export function getThumbnailUrl(slug: string): string {
  return `https://takazudomodular.com/images/p/${slug}/900w.webp`;
}

/**
 * Generate the enlarged image URL for a gallery item
 */
export function getEnlargedImageUrl(slug: string): string {
  return `https://takazudomodular.com/images/p/${slug}/2000w.webp`;
}

/**
 * Get items for preloading (current + next 2 + prev 2)
 */
export function getItemsForPreloading(slug: string): GalleryItem[] {
  const index = getGalleryItemIndex(slug);
  if (index === -1) return [];

  const items: GalleryItem[] = [];

  // Add previous 2 items
  for (let i = Math.max(0, index - 2); i < index; i++) {
    items.push(galleryData[i]);
  }

  // Add current item
  items.push(galleryData[index]);

  // Add next 2 items
  for (let i = index + 1; i <= Math.min(galleryData.length - 1, index + 2); i++) {
    items.push(galleryData[i]);
  }

  return items;
}
